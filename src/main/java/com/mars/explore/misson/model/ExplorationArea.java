package com.mars.explore.misson.model;

import java.util.List;

public class ExplorationArea {

	private List<Integer> longitude;
	private List<Integer> latitude;
	
	public List<Integer> getLongitude() {
		return longitude;
	}
	public void setLongitude(List<Integer> longitude) {
		this.longitude = longitude;
	}
	public List<Integer> getLatitude() {
		return latitude;
	}
	public void setLatitude(List<Integer> latitude) {
		this.latitude = latitude;
	}
	
}